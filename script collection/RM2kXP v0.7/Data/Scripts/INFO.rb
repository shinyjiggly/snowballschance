*********************** IMPORTANT THINGS TO KNOW **************************

- Normal XP Windowskins can't be used anymore. Otherwise there's a 001-Blue01
for this system.

- On Message Boxes, now you can use \C[n] for 0 to 19.
  
- Terrains are used on Neo_Mode: 2 & 3 = Vertical Neo_Mode Terrain and
  they all are also used on the Vehicle System, so be careful.
    
- If you name an Icon with "2k_(something)" the icon color will be shown as
  in 2k3, but beware the lag. The other icons are as default.

